/*
En este video estaremos viendo lo que son los arrow Functions!!

LAs arrow functions son otra forma de declarar funciones y fueron agregadas en las últimas versiones, la sintaxis es más corta y cuando comencé a utilizarlas me parecian algo complejas, en este video y los siguientes te mostraré todo lo que tienes que saber de arrow functions...
*/

const aprendiendo1 = function () {
  console.log("Aprendiendo JavaScript");
};

// Si tu función tiene una sola línea de código, puedes omitir las llaves y el return
const aprendiendo2 = () => "Aprendiendo JavaScript";
console.log(aprendiendo2());
